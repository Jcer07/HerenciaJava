    public void mostrarCelular(Celular celular) {
        equipoInformaticoEditar = celular;
        presentarDatos(celular);
    }
    
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(validarCampos() == true){
            if(equipoInformaticoEditar == null){
                Celular equipo = new Celular();
                equipo.setCodigo(UUID.randomUUID());
                equipo.setMarca(txtMarca.getText().trim());
                equipo.setModelo(txtModelo.getText().trim());
                equipo.setPrecio(Double.parseDouble(txtPrecio.getValue().toString()));
                equipo.setSistemaOperativo(cboSistemaOperativo.getSelectedItem().toString());
                equipo.setAlmacenamiento(txtAlmacenamiento.getText().trim());
                equipo.setCantidadCamaras(Integer.parseInt(txtNroCamaras.getValue().toString()));

                ListadoEquipos.Agregar(equipo);
                JOptionPane.showMessageDialog(this, "Datos guardados", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                limpiarCampos();
                frmCRUDNotebook.cargarListadoEnTable();
            }
            else{
                Celular celular = new Celular();
                celular.setCodigo(equipoInformaticoEditar.getCodigo());
                celular.setMarca(txtMarca.getText().trim());
                celular.setModelo(txtModelo.getText().trim());
                celular.setPrecio(Double.parseDouble(txtPrecio.getValue().toString()));
                celular.setSistemaOperativo(cboSistemaOperativo.getSelectedItem().toString());
                celular.setAlmacenamiento(txtAlmacenamiento.getText().trim());
                celular.setCantidadCamaras(Integer.parseInt(txtNroCamaras.getValue().toString()));

                if(ListadoEquipos.editarEquipo(celular)){
                    JOptionPane.showMessageDialog(this, "Datos Actualizados", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                    limpiarCampos();
                    frmCRUDNotebook.cargarListadoEnTable();
                }
                else{
                    JOptionPane.showMessageDialog(this, "No se pudo actualizar los datos", "Error", JOptionPane.ERROR_MESSAGE);
                    
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        limpiarCampos();
    }//GEN-LAST:event_btnCancelarActionPerformed

    
    private void presentarDatos(Celular celular){
        txtMarca.setText(celular.getMarca());
        txtModelo.setText(celular.getModelo());
        txtPrecio.setValue(celular.getPrecio());
        cboSistemaOperativo.setSelectedItem(celular.getSistemaOperativo());
        txtAlmacenamiento.setText(celular.getAlmacenamiento());
        txtNroCamaras.setValue(celular.getCantidadCamaras());
    }

    private boolean validarCampos(){
        if(txtMarca.getText().trim().length() == 0)
            return false;
        if(txtModelo.getText().trim().length() == 0)
            return false;
        if(txtPrecio.getValue().equals(0.0))
            return false;
        if(cboSistemaOperativo.getSelectedIndex() == 0)
            return false;
        if(txtAlmacenamiento.getText().trim().length() == 0)
            return false;
        if(txtNroCamaras.getValue().equals(0))
            return false;
        
        return true;
    }
    
    private void limpiarCampos(){
        txtMarca.setText("");
        txtModelo.setText("");
        txtPrecio.setValue(0);
        cboSistemaOperativo.setSelectedIndex(0);
        txtAlmacenamiento.setText("");
        txtNroCamaras.setValue(0);
        equipoInformaticoEditar = null;
    }
    
    
